export function generateNameForCountry(countryName, gender) {
    const country = CountriesData.find(c => c.CountryName === countryName);

    const firstNames = gender === 'male'
        ? country.MaleFirstNames
        : country.FemaleFirstNames;

    const lastNames = gender === 'male'
        ? country.MaleLastNames
        : country.FemaleLastNames;

    const firstName = firstNames[Math.floor(Math.random() * firstNames.length)];
    const lastName = lastNames[Math.floor(Math.random() * lastNames.length)];

    return `${firstName} ${lastName}`;
}